class Arithmetic{
	public static void main(String[] args)
	{
		int x=6;
		int y=2;

		int add=x+y;
		int sub=x-y;
		int mul=x*y;
		int div=x/y;

		System.out.println("The sum of" +x+ "+" +y+ " = " +add);
		System.out.println("The sum of" +x+ "-" +y+ " = " +sub);
		System.out.println("The sum of" +x+ "*" +y+ " = " +mul);
		System.out.println("The sum of" +x+ "/" +y+ " = " +div);

	}
}